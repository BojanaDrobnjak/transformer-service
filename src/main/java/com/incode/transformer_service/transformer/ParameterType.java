package com.incode.transformer_service.transformer;

/**
 * Enum representing possible types for a parameter.
 */
public enum ParameterType {

    /**
     * Represents a parameter of type String.
     */
    STRING,

    /**
     * Represents a parameter of type Integer.
     */
    INTEGER,

    /**
     * Represents a parameter of type Boolean.
     */
    BOOLEAN,

    /**
     * Represents a parameter of type Float.
     */
    FLOAT,

    /**
     * Represents a parameter of type Double.
     */
    DOUBLE
}
